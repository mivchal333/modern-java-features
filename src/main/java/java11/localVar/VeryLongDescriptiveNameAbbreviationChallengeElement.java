package java11.localVar;

public class VeryLongDescriptiveNameAbbreviationChallengeElement {
    private Long id;
    private String name;

    public VeryLongDescriptiveNameAbbreviationChallengeElement(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
